package operators;

public class ArthimeticOperators {

    public static void main(String[] args){
        int n1 = 10, n2 =6;
        System.out.println("n1 + n2 = "+(n1 + n2));
        System.out.println("n1 - n2 = "+(n1 - n2));
        System.out.println("n1 / n2 = "+(n1 / n2));
        System.out.println("n1 % n2 = "+(n1 % n2));
        System.out.println("n1 * n2 = "+(n1 * n2));
    }
}
